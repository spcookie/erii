package uesugi.core

enum class RouteRule(val title: String) {

    REQUEST_R18_IMAGE(
        """
        仅当用户【明确提出图片请求】时，才选择此分类。
        必须【同时满足】以下两个条件，缺一不可：

        条件一：存在明确的【索要图片的行为】
        例如：
        - “发图”
        - “来点图”
        - “给我看”
        - “有没有图片”
        - “来几张”

        条件二：图片内容明确指向【成人 / R18】
        例如：
        - “涩图”
        - “R18”
        - “黄图”
        - “色图”

        强制排除规则：
        - 如果内容仅包含成人、性、下流、攻击性、辱骂、玩梗等文字
        - 但【没有任何索要图片的意图或行为】
        则【绝对不要】归类为 REQUEST_R18_IMAGE。
        这种情况应优先归类为 CHAT。
        """.trimIndent()
    ),

    DIRECT_QA(
        """
        当用户提出的是【明确、严肃、可被直接回答的问题】时，选择此分类。

        判断标准（满足任一即可）：
        - 涉及知识、技术、编程、规则、事实、概念解释
        - 需要准确答案或理性说明
        - 去掉语气和情绪后，问题本身依然成立

        DIRECT_QA 的消息应由 AI 直接回答，
        【不进入任何人格 Agent】。
        即使语气随意，只要本质是求解答，也应归类为 DIRECT_QA。
        """.trimIndent()
    ),

    CHAT(
        """
        当消息不属于 REQUEST_R18_IMAGE 或 DIRECT_QA 时，默认归类为 CHAT。

        包括但不限于：
        - 群内日常闲聊、接话、调侃
        - 情绪表达、吐槽、发泄
        - 玩梗、辱骂、攻击性语言
        - 不追求严谨或标准答案的对话

        判断要点：
        - 更偏向互动、情绪或气氛
        - 即使内容不雅、粗俗或激烈
        - 只要不是索要图片，也不是严肃求解答
        都应归类为 CHAT，并进入人格 Agent 处理。
        """.trimIndent()
    )

}
